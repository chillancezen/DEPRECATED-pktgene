int reset_pktgene(int fd)
{
	int ret;
	ret=ioctl(fd,PKTGENE_CMD_GLOBAL_RESET);
	return ret;
}
int set_kthread_running_mask(int fd,int cpuno,int imask)
{
	int ret;
	int cmd=(((cpuno)<<16)&0xffff0000)|(imask&0xffff);
	ret=ioctl(fd,PKTGENE_CMD_GLOBAL_SETRUNINGMASK,cmd);
	return ret;
}
int attach_net_device(int fd,char *if_name,int icpuno)
{
	int ret;
	ret=ioctl(fd,PKTGENE_CMD_NETIF_GET_NETDEVICE,if_name);
	if(ret<0)
		goto local_ret;
	ret=ioctl(fd,PKTGENE_CMD_NETIF_ATTACH_NETDEVICE,icpuno);
	
	local_ret:
	return ret;
}
int attach_task_base(int fd,int ibaseno,int icpuno)
{
	int iret;
	int icmd=(((ibaseno)<<16)&0xffff0000)|(icpuno&0xffff);
	iret=ioctl(fd,PKTGENE_CMD_BASE_ASSIGN,icmd);
	return iret;
}
int allocate_task_base(int fd,int ino,int iframe_lenth,unsigned char * frame_buff,int imaxframeno)
{
	int ret;
	ret=ioctl(fd,PKTGENE_CMD_BASE_TASKBASE_NO,ino);
	if(ret<0)
		goto local_ret;
	ret=ioctl(fd,PKTGENE_CMD_BASE_FRAMESIZE,iframe_lenth);
	if(ret<0)
		goto local_ret;
	ret=ioctl(fd,PKTGENE_CMD_BASE_FRAME_BUFF,frame_buff);
	if(ret<0)
		goto local_ret;
	ret=ioctl(fd,PKTGENE_CMD_BASE_CSUM,0);/*enable csum in all fields*/
	if(ret<0)
		goto local_ret;
	ret=ioctl(fd,PKTGENE_CMD_BASE_MAX_FRAME_NO,imaxframeno);
	if(ret<0)
		goto local_ret;
	ret=ioctl(fd,PKTGENE_CMD_BASE_UPDTAE);
	if(ret<0)
		goto local_ret;
	local_ret:
	return ret;
}

