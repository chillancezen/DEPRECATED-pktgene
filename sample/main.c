	iret=ioctl(fd,PKTGENE_CMD_LINE_CURRENT_VAL,ivalue);
	if(iret<0)
		goto local_ret;
	iret=ioctl(fd,PKTGENE_CMD_LINE_DATA_ACTION_AND_SEEK,((data_action<<16)&0xffff0000)|(lseek_value&0xffff));
	if(iret<0)
		goto local_ret;
	
	iret=ioctl(fd,PKTGENE_CMD_LINE_UPDATE,ibaseno);
	local_ret:
	return iret;

